package AnimalRescuer;

public class Adopter {
   private String name;
   private float cash;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setCash(float cash) {
        this.cash = cash;
    }

    public float getCash() {
        return cash;
    }
}
